#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	long	i;

	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		i = nb;
		if (nb < 0)
		{
			ft_putchar('-');
			i = 0 - nb;
		}
		if (i > 10)
			ft_putnbr(i / 10);
		ft_putchar((i % 10) + 48);
	}
}
