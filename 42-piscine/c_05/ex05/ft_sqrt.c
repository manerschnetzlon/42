int	ft_sqrt(int nb)
{
	int	t;
	int	max;

	t = 1;
	max = 46340;
	if (nb < 1)
		return (0);
	while (t * t < nb && t <= max)
		t++;
	if (t * t == nb)
		return (t);
	else
		return (0);
}
