int	ft_is_prime(int nb)
{
	int	i;

	i = 3;
	if (nb < 2)
		return (0);
	while (i < nb)
	{
		if (nb % 2 == 0 || nb % i == 0)
			return (0);
		i += 2;
	}
	return (1);
}
