void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	buffer;

	i = 0;
	while (i < (size / 2))
	{	
		buffer = tab[i];
		tab[i] = tab[size - (i + 1)];
		tab[size - (i + 1)] = buffer;
		i++;
	}
}
